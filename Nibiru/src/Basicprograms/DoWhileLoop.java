package Basicprograms;

public class DoWhileLoop {

	public static void main(String[] args) {
		// Do..While Loop [Bottom Up approach]
/*What is the syntax?
 * do
 * {
 * 	increment/decrement statement;
 * }while(condition); //semicolon is important
 * 
 * How this works is, first round the statement in 'do' will be executed. 
 * Once the statement is executed, then the while condition is met and 
 * then again the while to do loop. 
 */		
		
		//Example-1: Print values from 1 to 10
		
		int i = 1; 
		do {
			System.out.println(i);
			i++;
		}while(i<=10);	
		
		
		//Example-2: Print values from 
		
	}

}
