		//double to float
		
		double c = 123.4567;
		System.out.println(c);
		
		System.out.println("Double to float  explicit casting"); 
		
		float d = (float)c; 	 	
		
		System.out.println(d);
		System.out.println("==================================");
		
		//integer to short 
		
		int e = 11; 
		System.out.println(e); 
	
		short f; 
